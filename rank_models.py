#!/usr/bin/env python3
"""Rank benchmarked models using weighted normalized metrics.

Default score weights:
- TPS (higher is better): 0.50
- Peak RSS (lower is better): 0.30
- TTFT (lower is better): 0.20
"""

from __future__ import annotations

import argparse
import csv
import os
from typing import Dict, List


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Rank models from comparison_results.csv")
    parser.add_argument(
        "--input-csv",
        default="comparison_results.csv",
        help="Input CSV generated by compare_models.py",
    )
    parser.add_argument(
        "--output-csv",
        default="comparison_ranked.csv",
        help="Output ranked CSV path",
    )
    parser.add_argument(
        "--w-tps",
        type=float,
        default=0.50,
        help="Weight for avg_tps (higher is better)",
    )
    parser.add_argument(
        "--w-rss",
        type=float,
        default=0.30,
        help="Weight for peak_process_rss_mb (lower is better)",
    )
    parser.add_argument(
        "--w-ttft",
        type=float,
        default=0.20,
        help="Weight for avg_ttft_s (lower is better)",
    )
    return parser.parse_args()


def to_float(value: str, default: float = 0.0) -> float:
    try:
        return float(value)
    except (TypeError, ValueError):
        return default


def min_max_norm(value: float, min_v: float, max_v: float, higher_is_better: bool) -> float:
    if max_v - min_v == 0:
        return 1.0
    if higher_is_better:
        return (value - min_v) / (max_v - min_v)
    return (max_v - value) / (max_v - min_v)


def load_ok_rows(path: str) -> List[Dict[str, str]]:
    if not os.path.isfile(path):
        raise FileNotFoundError(f"CSV not found: {path}")

    with open(path, "r", encoding="utf-8", newline="") as handle:
        rows = list(csv.DictReader(handle))

    ok_rows = [row for row in rows if row.get("status") == "ok"]
    if not ok_rows:
        raise ValueError("No successful benchmark rows found (status=ok).")

    return ok_rows


def validate_weights(w_tps: float, w_rss: float, w_ttft: float) -> None:
    for name, value in [("w_tps", w_tps), ("w_rss", w_rss), ("w_ttft", w_ttft)]:
        if value < 0:
            raise ValueError(f"{name} must be >= 0")
    total = w_tps + w_rss + w_ttft
    if total <= 0:
        raise ValueError("At least one weight must be > 0")


def rank_rows(rows: List[Dict[str, str]], w_tps: float, w_rss: float, w_ttft: float) -> List[Dict[str, str]]:
    tps_vals = [to_float(r.get("avg_tps", "0")) for r in rows]
    rss_vals = [to_float(r.get("peak_process_rss_mb", "0")) for r in rows]
    ttft_vals = [to_float(r.get("avg_ttft_s", "0")) for r in rows]

    min_tps, max_tps = min(tps_vals), max(tps_vals)
    min_rss, max_rss = min(rss_vals), max(rss_vals)
    min_ttft, max_ttft = min(ttft_vals), max(ttft_vals)

    scored: List[Dict[str, str]] = []
    for row in rows:
        tps = to_float(row.get("avg_tps", "0"))
        rss = to_float(row.get("peak_process_rss_mb", "0"))
        ttft = to_float(row.get("avg_ttft_s", "0"))

        tps_norm = min_max_norm(tps, min_tps, max_tps, higher_is_better=True)
        rss_norm = min_max_norm(rss, min_rss, max_rss, higher_is_better=False)
        ttft_norm = min_max_norm(ttft, min_ttft, max_ttft, higher_is_better=False)

        score = w_tps * tps_norm + w_rss * rss_norm + w_ttft * ttft_norm

        new_row = dict(row)
        new_row["score"] = f"{score:.6f}"
        new_row["norm_tps"] = f"{tps_norm:.6f}"
        new_row["norm_rss"] = f"{rss_norm:.6f}"
        new_row["norm_ttft"] = f"{ttft_norm:.6f}"
        scored.append(new_row)

    scored.sort(key=lambda r: to_float(r["score"]), reverse=True)

    for idx, row in enumerate(scored, start=1):
        row["rank"] = str(idx)

    return scored


def save_rows(path: str, rows: List[Dict[str, str]]) -> None:
    fields = [
        "rank",
        "name",
        "path",
        "status",
        "error",
        "score",
        "norm_tps",
        "norm_rss",
        "norm_ttft",
        "model_load_time_s",
        "avg_ttft_s",
        "avg_tps",
        "peak_process_rss_mb",
        "elapsed_wall_s",
    ]
    with open(path, "w", encoding="utf-8", newline="") as handle:
        writer = csv.DictWriter(handle, fieldnames=fields)
        writer.writeheader()
        writer.writerows(rows)


def main() -> int:
    args = parse_args()
    validate_weights(args.w_tps, args.w_rss, args.w_ttft)
    rows = load_ok_rows(args.input_csv)
    ranked = rank_rows(rows, args.w_tps, args.w_rss, args.w_ttft)
    save_rows(args.output_csv, ranked)

    print("=== Weighted Ranking ===")
    print(
        f"Weights -> TPS: {args.w_tps:.2f}, RSS: {args.w_rss:.2f}, TTFT: {args.w_ttft:.2f}"
    )
    for row in ranked:
        print(
            f"{row['rank']}. {row['name']} | score={row['score']} | TPS={row['avg_tps']} | TTFT={row['avg_ttft_s']}s | PeakRSS={row['peak_process_rss_mb']}MB"
        )

    print(f"Saved ranked CSV: {args.output_csv}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
