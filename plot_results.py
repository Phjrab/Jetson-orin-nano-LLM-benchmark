#!/usr/bin/env python3
"""Create benchmark comparison plots from comparison_results.csv."""

from __future__ import annotations

import argparse
import csv
import os
from typing import Dict, List


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Plot benchmark comparison results.")
    parser.add_argument(
        "--input-csv",
        default="comparison_results.csv",
        help="Input CSV generated by compare_models.py",
    )
    parser.add_argument(
        "--output-png",
        default="comparison_results.png",
        help="Output image path",
    )
    parser.add_argument("--dpi", type=int, default=170, help="PNG DPI")
    parser.add_argument(
        "--sort-by",
        default="avg_tps",
        choices=["avg_tps", "avg_ttft_s", "peak_process_rss_mb", "model_load_time_s"],
        help="Metric used for sorting bars",
    )
    return parser.parse_args()


def _to_float(value: str, fallback: float = 0.0) -> float:
    try:
        return float(value)
    except (TypeError, ValueError):
        return fallback


def load_rows(csv_path: str) -> List[Dict[str, str]]:
    if not os.path.isfile(csv_path):
        raise FileNotFoundError(f"CSV not found: {csv_path}")

    with open(csv_path, "r", encoding="utf-8", newline="") as handle:
        reader = csv.DictReader(handle)
        rows = list(reader)

    if not rows:
        raise ValueError("CSV is empty.")

    ok_rows = [row for row in rows if row.get("status") == "ok"]
    if not ok_rows:
        raise ValueError("No successful rows found (status=ok).")

    return ok_rows


def sort_rows(rows: List[Dict[str, str]], sort_by: str) -> List[Dict[str, str]]:
    reverse = sort_by == "avg_tps"
    return sorted(rows, key=lambda r: _to_float(r.get(sort_by, "0")), reverse=reverse)


def render_plot(rows: List[Dict[str, str]], output_png: str, dpi: int) -> None:
    try:
        import matplotlib.pyplot as plt
    except ImportError as exc:
        raise RuntimeError(
            "matplotlib is not installed. Run: pip install matplotlib"
        ) from exc

    names = [r.get("name", "unknown") for r in rows]
    tps = [_to_float(r.get("avg_tps", "0")) for r in rows]
    ttft = [_to_float(r.get("avg_ttft_s", "0")) for r in rows]
    rss = [_to_float(r.get("peak_process_rss_mb", "0")) for r in rows]
    load = [_to_float(r.get("model_load_time_s", "0")) for r in rows]

    fig, axes = plt.subplots(2, 2, figsize=(14, 9))
    fig.suptitle("Local LLM Comparison (Jetson Orin Nano)", fontsize=15, fontweight="bold")

    bar_color = "#2f6f8f"

    axes[0, 0].bar(names, tps, color=bar_color)
    axes[0, 0].set_title("Average TPS (higher is better)")
    axes[0, 0].set_ylabel("tokens/s")

    axes[0, 1].bar(names, ttft, color=bar_color)
    axes[0, 1].set_title("Average TTFT (lower is better)")
    axes[0, 1].set_ylabel("seconds")

    axes[1, 0].bar(names, rss, color=bar_color)
    axes[1, 0].set_title("Peak Process RSS")
    axes[1, 0].set_ylabel("MB")

    axes[1, 1].bar(names, load, color=bar_color)
    axes[1, 1].set_title("Model Load Time")
    axes[1, 1].set_ylabel("seconds")

    for ax in axes.flat:
        ax.tick_params(axis="x", labelrotation=20)
        ax.grid(axis="y", alpha=0.25)

    plt.tight_layout(rect=[0, 0, 1, 0.96])
    plt.savefig(output_png, dpi=dpi)
    plt.close(fig)


def main() -> int:
    args = parse_args()
    rows = load_rows(args.input_csv)
    rows = sort_rows(rows, args.sort_by)
    render_plot(rows, args.output_png, args.dpi)

    print(f"Saved plot: {args.output_png}")
    print("Tip: open the PNG and compare TPS with TTFT and RSS together.")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
